  avg(case when "Н_ВЕДОМОСТИ"."ОЦЕНКА" ~ '[2-5]' then "Н_ВЕДОМОСТИ"."ОЦЕНКА"::numeric end) > 4.75
  and
  count(case when "Н_ВЕДОМОСТИ"."ОЦЕНКА" = 'зачет' then 1 end) >= 0
  and
  count(case when "Н_ВЕДОМОСТИ"."ОЦЕНКА" in ('незач','осв','неявка') then 1 end) = 0
  and 
  count(case when "Н_ВЕДОМОСТИ"."ОЦЕНКА" in null then 1 end) = 0
