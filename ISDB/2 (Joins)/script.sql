--Task 4
--Найти группы, в которых в 2011 году было ровно 10 обучающихся студентов на кафедре вычислительной техники.
--Для реализации использовать соединение таблиц.
select "Н_УЧЕНИКИ"."ГРУППА" from "Н_УЧЕНИКИ"
where "Н_УЧЕНИКИ"."ГРУППА" in (
  select "Н_УЧЕНИКИ"."ГРУППА" from "Н_УЧЕНИКИ"
    join "Н_ПЛАНЫ" on "Н_ПЛАНЫ"."ИД" = "Н_УЧЕНИКИ"."ПЛАН_ИД"
    join "Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ" on "Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ"."ИД" = "Н_ПЛАНЫ"."НАПС_ИД"
    join "Н_НАПР_СПЕЦ" on "Н_НАПР_СПЕЦ"."ИД" = "Н_НАПРАВЛЕНИЯ_СПЕЦИАЛ"."НАПС_ИД"
  where "Н_НАПР_СПЕЦ"."НАИМЕНОВАНИЕ" in ('Информатика и вычислительная техника','ИНФОРМАТИКА И ВЫЧИСЛИТЕЛЬНАЯ ТЕХНИКА') and "Н_ПЛАНЫ"."УЧЕБНЫЙ_ГОД" = '2011/2012'
  group by "Н_УЧЕНИКИ"."ГРУППА"
  having count("Н_УЧЕНИКИ"."ГРУППА") = 10
)
group by "Н_УЧЕНИКИ"."ГРУППА";



--Task 5
--Выведите таблицу со средними оценками студентов группы 4100 (Номер, ФИО, Ср_оценка), у которых средняя оценка не больше максимальной оценк(е|и) в группе 1101.
select "ЧЛВК_ИД", "ФАМИЛИЯ" || ' ' || "ИМЯ" || ' ' || "ОТЧЕСТВО" as "ФИО", avg("ОЦЕНКА"::numeric) from "Н_ВЕДОМОСТИ"
    join "Н_ЛЮДИ" on "Н_ВЕДОМОСТИ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
    join "Н_УЧЕНИКИ" using("ЧЛВК_ИД")
where "ОЦЕНКА" ~ '[2-5]' and "Н_УЧЕНИКИ"."ГРУППА" = '4100'
group by "ЧЛВК_ИД", "ФАМИЛИЯ", "ИМЯ", "ОТЧЕСТВО" 
having avg("ОЦЕНКА"::numeric) <= (
  select max("ОЦЕНКА"::numeric) from "Н_ВЕДОМОСТИ"
    join "Н_УЧЕНИКИ" using ("ЧЛВК_ИД")
  where "Н_УЧЕНИКИ"."ГРУППА" = '1101' and "ОЦЕНКА" ~ '[2-5]'
);

