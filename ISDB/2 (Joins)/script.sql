--Task 1
--Сделать запрос для получения атрибутов из указанных таблиц, применив фильтры по указанным условиям:
--Таблицы: Н_ЛЮДИ, Н_СЕССИЯ.
--Вывести атрибуты: Н_ЛЮДИ.ОТЧЕСТВО, Н_СЕССИЯ.ЧЛВК_ИД.
--Фильтры (AND):
--a) Н_ЛЮДИ.ОТЧЕСТВО < Сергеевич.
--b) Н_СЕССИЯ.ДАТА < 2004-01-17.
--Вид соединения: RIGHT JOIN         

select "Н_ЛЮДИ"."ОТЧЕСТВО", "Н_СЕССИЯ"."ЧЛВК_ИД" from "Н_ЛЮДИ"
	right join "Н_СЕССИЯ" on "Н_СЕССИЯ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
where "Н_ЛЮДИ"."ОТЧЕСТВО" < 'Сергеевич' and "Н_СЕССИЯ"."ДАТА" < '2004-01-17';

-- Task 2
--Сделать запрос для получения атрибутов из указанных таблиц, применив фильтры по указанным условиям:
--Таблицы: Н_ЛЮДИ, Н_ОБУЧЕНИЯ, Н_УЧЕНИКИ.
--Вывести атрибуты: Н_ЛЮДИ.ИМЯ, Н_ОБУЧЕНИЯ.НЗК, Н_УЧЕНИКИ.НАЧАЛО.
--Фильтры: (AND)
--a) Н_ЛЮДИ.ФАМИЛИЯ > Ёлкин.
--b) Н_ОБУЧЕНИЯ.НЗК > 001000.
--c) Н_УЧЕНИКИ.ГРУППА = 1100.
--Вид соединения: RIGHT JOIN.        

select "Н_ЛЮДИ"."ИМЯ", "Н_ОБУЧЕНИЯ"."НЗК", "Н_УЧЕНИКИ"."НАЧАЛО" from "Н_ОБУЧЕНИЯ"
	right join "Н_ЛЮДИ" on "Н_ЛЮДИ"."ИД" = "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД"	
	right join "Н_УЧЕНИКИ" on "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" = "Н_УЧЕНИКИ"."ЧЛВК_ИД"
	where "Н_ЛЮДИ"."ФАМИЛИЯ" > 'Ёлкин' and "Н_ОБУЧЕНИЯ"."НЗК" > '001000' and  "Н_УЧЕНИКИ"."ГРУППА" = '1100';

